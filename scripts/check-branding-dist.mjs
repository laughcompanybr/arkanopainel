#!/usr/bin/env node
/**
 * Post-build branding guard — scans compiled output (dist/, .output/) for
 * any forbidden authorship strings. Developed by Laugh Company
 *
 * Runs after `vite build`. Fails the build (exit 1) if any match is found
 * in JS/CSS/HTML/JSON assets. Package-name residues from external tooling
 * are allow-listed narrowly.
 */
import { existsSync, readFileSync, readdirSync, statSync } from "node:fs";
import { extname, join, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(fileURLToPath(import.meta.url), "..", "..");
const CANDIDATES = ["dist", ".output", ".vinxi"];
const SCAN_EXT = new Set([".js", ".mjs", ".cjs", ".css", ".html", ".json", ".txt", ".map"]);

const FORBIDDEN = [
  /\blovable\b/i,
  /\bgenerated by\b/i,
  /\bauto-?generated\b/i,
  /\bpowered by ai\b/i,
  /\bmade with ai\b/i,
];

// External package-name residues that legitimately survive bundling
// because vite emits config-derived module ids in source maps / manifests.
// Keep tiny and specific.
const ALLOW_SUBSTRINGS = [
  "@lovable.dev/",           // npm package identifier
  "__lovableEvents",         // runtime host bridge global
];

function isAllowedLine(line) {
  return ALLOW_SUBSTRINGS.some((s) => line.includes(s));
}

function walk(dir, hits) {
  for (const name of readdirSync(dir)) {
    const abs = join(dir, name);
    const st = statSync(abs);
    if (st.isDirectory()) {
      walk(abs, hits);
      continue;
    }
    if (!SCAN_EXT.has(extname(name))) continue;
    const rel = relative(ROOT, abs).split(sep).join("/");
    const content = readFileSync(abs, "utf8");
    // fast-path: no lowercase "lovable" / "generated" anywhere
    if (!/lovable|generated|powered by ai|made with ai/i.test(content)) continue;
    const lines = content.split(/\r?\n/);
    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];
      if (isAllowedLine(line)) continue;
      for (const rx of FORBIDDEN) {
        const m = line.match(rx);
        if (m) hits.push({ file: rel, line: i + 1, match: m[0], snippet: line.trim().slice(0, 180) });
      }
    }
  }
}

const targets = CANDIDATES.map((c) => join(ROOT, c)).filter(existsSync);
if (targets.length === 0) {
  console.log("· Post-build guard: no build output found (skip).");
  process.exit(0);
}

const hits = [];
for (const dir of targets) walk(dir, hits);

if (hits.length) {
  console.error("\n✖ Post-build branding guard FAILED — forbidden strings in build output:\n");
  for (const h of hits.slice(0, 40)) {
    console.error(`  ${h.file}:${h.line}  [${h.match}]  ${h.snippet}`);
  }
  if (hits.length > 40) console.error(`  … and ${hits.length - 40} more`);
  console.error(`\n${hits.length} violation(s) in compiled assets. Investigate before releasing.\n`);
  process.exit(1);
}

console.log(`✓ Post-build guard passed — no forbidden strings in ${targets.length} output dir(s).`);
